using Nop.Services.Caching;
using NopAdvance.Plugin.Misc.PublicAPI.Domain;

namespace NopAdvance.Plugin.Misc.PublicAPI.Infrastructure.Caching;

public partial class APIApplicationCacheEventConsumer : CacheEventConsumer<APIApplication>
{
    /// <summary>
    /// Clear cache data
    /// </summary>
    /// <param name="entity">Entity</param>
    /// <param name="entityEventType">Entity event type</param>
    /// <returns>A task that represents the asynchronous operation</returns>
    protected override async Task ClearCacheAsync(APIApplication entity, EntityEventType entityEventType)
    {
        await RemoveAsync(CachingDefaults.APIApplicationByAPIKeyCacheKey, entity.APIKey);
        await RemoveByPrefixAsync(string.Format(CachingDefaults.APIRefreshTokenPrefix, entity.Id));

        await base.ClearCacheAsync(entity, entityEventType);
    }
}
