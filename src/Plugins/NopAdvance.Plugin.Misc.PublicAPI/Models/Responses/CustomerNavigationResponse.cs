using Nop.Web.Framework.Models;
using Nop.Web.Models.Customer;

namespace NopAdvance.Plugin.Misc.PublicAPI.Models.Responses;

public record CustomerNavigationResponse : BaseNopModel
{
    public CustomerNavigationResponse()
    {
        CustomerNavigationItems = new List<CustomerNavigationItemResponse>();
    }

    public IList<CustomerNavigationItemResponse> CustomerNavigationItems { get; set; }

    public CustomerNavigationEnum SelectedTab { get; set; }
}
public record CustomerNavigationItemResponse : BaseNopModel
{
    public string RouteName { get; set; }
    public string Title { get; set; }
    public CustomerNavigationEnum Tab { get; set; }
    public string ItemClass { get; set; }
}
