using System.Security.Cryptography;
using MimeKit;

namespace NopAdvance.Plugin.Misc.PublicAPI.Services;

public static class PluginCommonHelper
{
    public static (byte[], string, int) ConvertBase64ToFile(string base64String, string fileName)
    {
        var mimeType = MimeTypes.GetMimeType(fileName);
        if (!string.IsNullOrWhiteSpace(mimeType))
        {
            var fileBytes = Convert.FromBase64String(base64String);
            return (fileBytes, mimeType, fileBytes.Length);
        }
        return (null, "", 0);
    }

    public static string GenerateSecretKey()
    {
        var randomNumber = new byte[32];
        using var rng = RandomNumberGenerator.Create();
        rng.GetBytes(randomNumber);
        return Convert.ToBase64String(randomNumber);
    }
}
