using FluentMigrator.Builders.Create.Table;
using Nop.Core.Domain.Customers;
using Nop.Data.Mapping.Builders;
using NopAdvance.Plugin.Misc.PublicAPI.Domain;
using Nop.Data.Extensions;
using Nop.Core.Domain.Stores;

namespace NopAdvance.Plugin.Misc.PublicAPI.Data;

public class APIDebugBuilder : NopEntityBuilder<APIDebugLog>
{
    #region Methods

    /// <summary>
    /// Apply entity configuration
    /// </summary>
    /// <param name="table">Create table expression builder</param>
    public override void MapEntity(CreateTableExpressionBuilder table)
    {
        table
            .WithColumn(nameof(APIDebugLog.CustomerId)).AsInt32().Nullable().ForeignKey<Customer>()
            .WithColumn(nameof(APIDebugLog.StoreId)).AsInt32().Nullable().ForeignKey<Store>();
    }

    #endregion
}
